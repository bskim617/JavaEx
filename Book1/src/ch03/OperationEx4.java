package ch03;

public class OperationEx4 {
	//조건 연산자
	public static void main(String[] args) {
		int fatherAge = 45;
		int motherAge = 47;
		
		char ch;
		ch = (fatherAge > motherAge) ? 'T' : 'F';
		
		System.out.println(ch);
		
		/*
		 * 조건 연산자
		 * - 조건식 ? 결과1 : 결과 2
		 * - 조건식이 참이면 결과1이 거짓이면 결과2가 선택
		 */
		
	}
}
